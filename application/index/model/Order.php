        //E  5:充值
        //F  6:买断(余额)
        //G  7:结算租金(余额)
        //H  8:提现
        switch ($data['type']) {
            case '1':
                if (empty($data['cid'])) {
                    return returnJson(900, 400, '押金Id不能为空');
                }

                $config = \app\admin\model\Config::get($data['cid']);

