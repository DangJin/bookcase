    public function compDeposit($order)
    {
        $user = User::get($order->getAttr('uid'));

        if (is_null($user))
            return [
                'code' => 606,
                'status' => 400,
                'msg' => '订单用户错误'
            ];

        $this->startTrans();
        try {
            $user->deposit = $user->deposit + $order->getAttr('amount');
            $user->isUpdate(true)->save();
            $order->state = 2;
            $order->isUpdate(true)->save();
            $this->commit();
        } catch (Exception $e) {
            $this->rollback();
            return [
                'code' => 606,
                'status' => 400,
                'msg' => $e->getMessage()
            ];
        }
        return [
            'code' => 606,
            'status' => 200,
            'msg' => '完成订单'
        ];
    }

